    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String chemistry = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();
        
        
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","KILLER_7sv08");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into results(rollno,Physics,Maths,Chemistry,EM,DBMS) values('"+rollno+"','"+physics+"','"+maths+"','"+chemistry+"','"+em+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null ,"Result is Saved");
            }else{
                JOptionPane.showMessageDialog(null ,"Student is not Registerd");
            }
            
            setVisible(false);
            new InsertResult().setVisible(true);    
        }catch(Exception e){
            JOptionPane.showMessageDialog(null , e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

